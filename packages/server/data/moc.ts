export const changeTimeData = () => {
  const newTimeSLA = timeSLAs.map(({ id, timeSLA }) => {
    const arrSLA = timeSLA.split(/[. :]+/)
    if (arrSLA[3].length < 2) {
      arrSLA[3] = `0${arrSLA[3]}`
    }
    const str = `${arrSLA[2]}-${arrSLA[1]}-${arrSLA[0]} ${arrSLA[3]}:${arrSLA[4]}:${arrSLA[5]}.000+00`
    return { id, timeSLA: str }
  })
  return newTimeSLA
}
